# Input two people's birth years and calculate the age difference between them.

person_1 = input("Person 1, what is your age?? ")
person_2 = input("Person 2, what is your age?? ")
person_1 = int(person_1)
person_2 = int(person_2)

if person_1 > person_2:
    print(person_1 - person_2)
else:                           # person_2 > person_1 or person_2 == person_1
    print(person_2 - person_1)

