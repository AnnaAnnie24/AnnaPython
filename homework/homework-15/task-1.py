# Ask the user for 5 numbers, then count the average of those numbers

number_1 = input("Please put in a number: ")
number_2 = input("Please put in another number: ")
number_3 = input("Please put in a third number: ")
number_4 = input("Please put in a number: ")
number_5 = input("Please put in another number: ")
number_1 = int(number_1)
number_2 = int(number_2)
number_3 = int(number_3)
number_4 = int(number_4)
